#include "libft.h"

int		int_len(intmax_t n)
{
	int size;

	size = 0;
	if (n == 0)
		return (1);
	if (n < 0)
		n = -n;
	while (n > 0)
	{
		n = n / 10;
		size++;
	}
	return (size);
}
